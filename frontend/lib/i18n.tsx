const DICTS: Record<Lang, Dict> = { en, hi, pt, es };

export function getLang(): Lang {
  if (typeof window === "undefined") return "en";
  try {
    const l = localStorage.getItem(LANG_KEY) as Lang | null;
    return l && DICTS[l] ? l : "en";
  } catch { return "en"; }
}

export function setLang(code: Lang): void {
  try {
    localStorage.setItem(LANG_KEY, code);
    window.dispatchEvent(new Event("payqr:lang"));
  } catch {}
}

/** translate a key for an explicit language (EN fallback, then the key). */
export function translate(key: string, lang: Lang): string {
  return DICTS[lang]?.[key] ?? en[key] ?? key;
}

/**
 * Hook: returns { t, lang, setLang }. `t(key)` translates in the current
 * language; the component re-renders when the language changes anywhere.
 */
export function useT() {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    setLangState(getLang());
    const onChange = () => setLangState(getLang());
    window.addEventListener("payqr:lang", onChange);
    window.addEventListener("storage", onChange);
    return () => {
      window.removeEventListener("payqr:lang", onChange);
      window.removeEventListener("storage", onChange);
    };
  }, []);

  const t = useCallback((key: string) => translate(key, lang), [lang]);
  const change = useCallback((code: Lang) => { setLang(code); setLangState(code); }, []);

  return { t, lang, setLang: change };
}
